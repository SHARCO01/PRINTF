#include "printf.h"

int	printf_int(int n)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return 0;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	return (printf_int((n % 10) + '0'));
}
